#Soma dos números de 2 até 14:

#A soma dos números de 2 até 14 pode ser calculada usando a fórmula da soma de uma progressão aritmética:

#n = (n / 2) * (a1 + an)

#Onde:
#- a1 = 2 (primeiro termo)
#- an = 14 (último termo)
#- n = 14 - 2 + 1 = 13 (quantidade de termos)

#Substituindo os valores:

#Sn = (13 / 2) * (2 + 14) 
#Sn= (13 / 2) * 16 
#Sn= 13 * 8 = 104

#Portanto, a soma dos números de 2 até 14 é 104.
