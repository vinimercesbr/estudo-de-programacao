#Para determinar a quantidade de termos em uma progressão aritmética, usamos a seguinte fórmula:

#n = (an - a1) / r + 1

#Onde:
#- n é o número total de termos da progressão
#- a1 é o primeiro termo
#- an é o último termo
#- r é a razão da progressão

#Exemplo:
#Se temos uma progressão aritmética com primeiro termo a₁ = 2, último termo aₙ = 14 e razão r = 2, então:

#n = (14 - 2) / 2 + 1 
#n = 4

#Portanto, essa progressão tem 4 termos.
